package org.pbatch.classelements.variables.local;

/*
                   defaultValues
  Integer :
    byte            0
    short           0
    int             0
    long            0

  Decimal :
   float            0.0
   double           0.0

   Binary: True/False
   boolean          false

   Character:
   char              ''

 */
public class PrimitiveDataTypes {

    public static void main(String[] args) {
        byte b = -128; // 1 byte
        short s = 2;   //
        int i = 3;   // 4 byte
        long l = 4;  //8 byte

        System.out.println(b);
        System.out.println(s);
        System.out.println(i);
        System.out.println(l);
    }

}
